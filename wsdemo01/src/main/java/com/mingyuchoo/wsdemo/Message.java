package com.mingyuchoo.wsdemo;

public class Message {

    private String name;

    // Constructor
    public Message() {}

    public Message(String name) {
        this.name = name;
    }

    // Getter & Setter
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
